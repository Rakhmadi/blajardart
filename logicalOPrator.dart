void main(){
  int  x =12;
  int y =112;
  //and logical
  if (x>100  &&  y>100) {
    print('true');
  }else{
    print("false");
  }
  //or Logicaal
  if (x>100  ||  y>100) {
    print('true');
  }else{
    print("false");
  }
  //not logical
  if (!(x>100)) {
    print('true');
  }else{
    print("false");
  }

}